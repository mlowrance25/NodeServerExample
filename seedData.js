exports.getUsers = function(numUsers){
    let users = [];
    for(let index=0;index<numUsers;index++){
        let firstName = getName();
        let lastName = getName();
        let companyName = `${firstName} ${lastName}'s Company`
        users.push({FirstName:firstName,LastName:lastName,Company : companyName})
    }
    return users;
}

